package hu.openig.model;

/**
 * The enumeration to refer to a cursor for switching.
 * @author danyetz, 2014.07.21.
 */
public enum Cursors {
    /** Blank cursor. */
    BLANK,
    /** Hand cursor. */
    HAND,
    /** Move cursor. */
    MOVE,
    /** Pointer cursor. */
    POINTER,
    /** Redo cursor. */
    REDO,
    /** Select cursor. */
    SELECT,
    /** Target cursor. */
    TARGET,
    /** Arrow down. */
    ARROW_S,
    /** Arrow left. */
    ARROW_W,
    /** Arrow up. */
    ARROW_N,
    /** Arrow right. */
    ARROW_E,
    /** Arrow up-right. */
    ARROW_NE,
    /** Arrow up-left. */
    ARROW_NW,
    /** Arrow down-right. */
    ARROW_SE,
    /** Arrow down-left. */
    ARROW_SW
}
